function TABSUM()
{
    $tab = array();

    for ($i = 1; $i <= 12; $i++) {
        for ($j = 1; $j <= 8; $j++) {
            $tab[$i][$j] = rand(0, 500);;
            $tab[$i][$j] . " " . " \n ";
        }
        
    }

    // Aplatir le tableau 2D en un tableau 1D pour trouver la valeur maximale
    $flatArray = array();
    for ($i = 1; $i <= 12; $i++) {
        for ($j = 1; $j <= 8; $j++) {
            $flatArray[] = $tab[$i][$j];
        }
    }

    // Trouver et afficher la valeur maximale
    $SumValue = array_sum($flatArray);
    return "La valeur maximale est : " . $SumValue;
};
